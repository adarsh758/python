number = 10

# if 
if number > 0:
    print("The number is positive")


if 10 > 6: 
    print("10 is bigger than 6")
    
# if-else
if number > 15:
    print(f"{number}, is bigger than 15")
else : 
    print(f"{number} is smaller than 15")
    
# if-elif
if number > 15:
    print(f"{number}, is bigger than 15")
elif number > 5 : 
    print(f"{number} is smaller than 5")
    
# if-elif-else
if number > 15:
    print(f"{number}, is bigger than 15")
elif number > 50 : 
    print(f"{number} is smaller than 5")
else :
    print(f"The number is: {number}")