# Arithmetic
a = 5 + 3
b = 5 - 3
c = 5 * 3
d = 5 / 2
e = 5 // 2
f = 5 % 2
g = 5 ** 3

print(a) # 8
print(b) # 2
print(c) # 15
print(d) # 2.5
print(e) # 2
print(f) # 1
print(g) # 125

# Logical
x = True and False
y = True or False
z = not True
k = not False

print(x) # False
print(y) # True
print(z) # False
print(k) # True
